#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

// Read the definition-wrapper.js file
const definitionWrapperPath = path.join(__dirname, '../../../definition-wrapper.js');
const fileContent = fs.readFileSync(definitionWrapperPath, 'utf8');

// Extract the pricingTerms array using regex
const pricingTermsMatch = fileContent.match(/const pricingTerms = (\[[\s\S]*?\n\]);/);
if (!pricingTermsMatch) {
  console.error('Could not find pricingTerms array in definition-wrapper.js');
  process.exit(1);
}

// Evaluate the pricingTerms array
const pricingTerms = eval(pricingTermsMatch[1]);

// Build the MDX content
const frontmatter = `---
title: Pricing Glossary
sidebarTitle: Pricing Glossary
description: Definitions for every feature and metric that appears in the ngrok pricing docs.
---`;

const comment = `{/* This file is generated by custom/scripts/glossary/generate-pricing-glossary.js. Do not edit it directly. */}`;

const intro = `Definitions for every feature and metric that appears in the [ngrok pricing docs](/pricing-limits/).`;

let glossaryContent = `${frontmatter}\n\n${comment}\n\n${intro}\n\n`;

// Add each term as an H2 with meaning and optional Learn More link
pricingTerms.forEach(term => {
  const heading = term.titles[0];
  const meaning = term.meaning || 'Definition not available';
  let link = term.link || '';

  // Trim /docs prefix from internal links (Mintlify convention)
  if (link.startsWith('/docs')) {
    link = link.substring(5);
  }

  glossaryContent += `## ${heading}\n\n`;
  glossaryContent += `${meaning}\n\n`;

  if (link) {
    glossaryContent += `[Learn More](${link})\n\n`;
  }
});

// Write pricing-glossary.mdx into pricing-limits/
const outputPath = path.join(__dirname, '../../../pricing-limits/pricing-glossary.mdx');
fs.writeFileSync(outputPath, glossaryContent.trim() + '\n');

console.log('Successfully generated pricing-limits/pricing-glossary.mdx');
